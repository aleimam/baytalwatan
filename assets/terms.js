  function reqRow(city, r){
    const area = r.area ? `<div class="br-area">${r.area}</div>` : '';
    return `<tr><td class="c">${city}</td><td>${area}</td><td class="num">${r.ratio}</td><td>${r.floors}</td>`
      + `<td class="num">${r.f} / ${r.b} / ${r.s} م</td></tr>`;
  }
  function tableHtml(){
    const rows = Object.entries(REQS).map(([city, list]) => list.map(r => reqRow(city, r)).join('')).join('');
    return `<div class="tbl-wrap"><table class="data adtbl br-table"><thead><tr>`
      + `<th>${t('t_city')}</th><th>${t('br_area')}</th><th>${t('br_ratio')}</th><th>${t('br_floors')}</th><th>${t('br_setbacks')} (${t('br_fbs')})</th>`
      + `</tr></thead><tbody>${rows}</tbody></table></div>`;
  }
  function premiumHtml(){
    return `<ul class="terms-list">` + PREMIUM.map(p=>`<li><b>${p.pct}</b> — ${I18N.lang==='en'?p.en:p.ar}</li>`).join('') + `</ul>`;
  }

  function render(el){
    if(!el) return;
    const html = SECTIONS.map(sec=>{
      let body = `<ul class="terms-list">${sec.items.map(x=>`<li>${x}</li>`).join('')}</ul>`;
      if(sec._table)   body += tableHtml();
      if(sec._premium) body += premiumHtml();
      return `<section class="terms-sec"><h3>${sec.h}</h3>${body}</section>`;
    }).join('');
    el.innerHTML = `<div class="terms-note">${t('terms_note')}</div>` + html;
  }

  return { forPlot, render, REQS };
